package com.wipro.celona.model;

public class CellEvents {
	
	private String enbId;
	
	private String cellEventType;
	
	private String description;

	public String getEnbId() {
		return enbId;
	}

	public void setEnbId(String enbId) {
		this.enbId = enbId;
	}

	public String getCellEventType() {
		return cellEventType;
	}

	public void setCellEventType(String cellEventType) {
		this.cellEventType = cellEventType;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

}
